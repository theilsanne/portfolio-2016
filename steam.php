                        <h2>Understanding</h2>
                        
                        <p>
                            Although this project was pitched as largely a visual or aesthetic project, I decided to take it further by understanding the user base and its concerns, since I had a good sample size of users.
                        </p>
                        
                        <div class="box half leftBorder">
                            <strong>“You can do so much more with the client - it’s more personalised than the website.”</strong>
                            <br /><br />
                            <span class="highlight"><em>A large downfall of the website is how impersonal it is.</em></span>
                        </div>
                        
                        <div class="box half leftBorder">
                            <strong>“The website exists solely to let you download the desktop client.”</strong>
                            <br /><br />
                            <span class="highlight"><em>The website does not give the user other reasons to use it.</em></span>
                        </div>
                        
                        <br /><br />
                        
                        <div class="box half leftBorder">
                            <strong>“The client has all my games - why would I need to use the website?”</strong>
                            <br /><br />
                            <span class="highlight"><em>User is more interested in accessing the physical games they own, rather than browsing games on the website.</em></span>
                        </div>
                        
                        <div class="box half leftBorder">
                            <strong>“The website is [unattractive].”</strong>
                            <br /><br />
                            <span class="highlight"><em>The website is not visually appealing enough to keep the user’s attention.</em></span>
                        </div>
                    </div>
                    
