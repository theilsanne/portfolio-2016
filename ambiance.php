                        <h2>Brief</h2>
                        <blockquote>Campuses need to encourage more social media interaction, promotion, and aura around its campus life.</blockquote>
                        
                        <p>
                            Ambiánce provides a way to visualise the social media coverage of events around campus through a mobile application and interactive visualisation table, using geo-location and hashtag data taken from social media. 
                        </p>
                        
                        <br />
                        
                        <a href="assets/media/ambiance/both-devices-mockup.png" data-lightbox="ambiance" data-title="Final Ambiánce design mockup"><img src="assets/media/ambiance/both-devices-mockup.png" alt="Final Ambiánce design mockup" /></a>
