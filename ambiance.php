                    <div class="box single">
                        <h2>Understanding</h2>
                        
                        <p>
                            A large part of the current issue with the lack of social media presence is due to the lack of centralisation of social media content across platforms.
                        </p>
                        
                        <p>
                            Having conducted a questionnaire to gauge the campus' social media impact on its students, I found that event awareness is limited despite users following some form of student life/campus life account. With this in mind, I proposed the following features:
                        </p>
                        
                        <div class="box half leftBorder">
                            <strong>Mobile application</strong><br />
                            <ul>
                                <li>Augmented reality camera to visualise aura in person</li>
                                <li>Ability to synchronise social media platforms</li>
                                <li>Social media feed built from source hashtag/geolocation data</li>
                                <li>Real time updating of visualisation</li>
                                <li>History of campus aura (daily, weekly, monthly)</li>
                            </ul>
                        </div>
                        
                        <div class="box half leftBorder">
                            <strong>Installation</strong><br />
                            <ul>
                                <li>AView large and/or trending eventsn</li>
                                <li>History of campus aura</li>
                                <li>Shows an overview of the aura</li>
                            </ul>
                        </div>
                    </div>
                    
